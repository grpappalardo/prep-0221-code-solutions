function addTwoNumbers(num1, num2) {
  return num1 + num2;
}

console.log(addTwoNumbers(4, 4));

function convertHoursToMinutes(number) {
  return number * 60;
}

console.log(convertHoursToMinutes(2));

function personalizeGreeting(name) {
  return ('Hello ' + name + '!');
}

console.log(personalizeGreeting('Erin'));

function addAndMultiplyBy5(num1, num2) {
  return ((num1 + num2) * 5);
}

console.log(addAndMultiplyBy5(2, 3));

function multiplyAndDivideBy5(num1, num2) {
  return ((num1 * num2) / 5);
}

console.log(multiplyAndDivideBy5(10, 2));

function subtractTwoNumbers(num1, num2) {
  return num1 - num2;
}

console.log(subtractTwoNumbers(10, 2));

function getCircleCircumference(radius) {
  return Math.PI * 2 * radius;
}

console.log(getCircleCircumference(3));

function getFullName(firstName, lastName) {
  return firstName + ' ' + lastName;
}

console.log(getFullName('Rachael', 'Grover'));

function cube(number) {
  return number ** 3;
}

console.log(cube(2));
